package arrayspack;

public class DemoThree {

	public static void main(String[] args) {
		
		//Creating single dimensional array
		int[] ar = new int[6];
		
		//Initialing the single dimensional array
		ar[0] = 9;
		ar[1] = 5;
		ar[2] = 3;
		ar[3] = 7;
		ar[4] = 1;
		ar[5] = 2;
		
		//Access the data stored in the single dimensional array
		System.out.println(ar[0]); //9 
		System.out.println(ar[1]); //5
		System.out.println(ar[2]); //3
		System.out.println(ar[3]); //7
		System.out.println(ar[4]); //1
		System.out.println(ar[5]); //2
		
	}

}

